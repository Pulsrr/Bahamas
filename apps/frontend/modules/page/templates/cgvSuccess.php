<h2>Conditions Générales de Vente - Allocab.com S.A.S.</h2>

<h3>1- Objet du service</h3>
<p>La société Allocab.com propose par le biais d’une plateforme automatisée sur internet
de mettre en relation l’offre et la demande sur le marché des transports de personne.
En d’autres termes Allocab.com propose à ses clients (particuliers ou entreprises) un
service de réservation automatique de location de véhicule motorisé avec chauffeur afin
de se rendre à une destination donnée.</p>

<h3>2- Accès au service</h3>
<p>Le Client s’il souhaite utiliser le Service, commande un véhicule par le biais de la
plateforme allocab.com, en remplissant les champs utilisateurs ainsi que le trajet à
effectuer.</p>
<p>
En cas d’annulation moins d’une heure avant le rendez-vous prévu, le client ou
l’entreprise pourra se voir infligé un malus sur son compte utilisateurs susceptible de
majorer le prix de sa prochaine réservation de course.</p>


<p>Pour que la course puisse avoir lieu, le client devra impérativement réserver sa course
sur le site internet : www.allocab.com mais également remplir ses champs utilisateurs et
créer son compte utilisateur.</p>

<p>Pour être prise en compte la demande de réservation devra impérativement se faire au
plus tard 40 min avant l’heure de demande de prise en charge effective de la personne.</p>

<p>Une confirmation de la bonne réception de la demande de réservation ainsi que la
confirmation de l’acceptation de la commande sera adressée au client par sms et par
mail.</p>

<p>Dans le cas ou Allocab.com ne serait pas en mesure d’assurer convenablement, et
conformément à sa prestation, la course commandée par le client, ce dernier devra
obligatoirement en être informé par Allocab.com dans un délai de 30 minutes avant la
course commandée.</p>

<h3>3- Tarifs</h3>
<p>Les tarifs des courses proposés par les chauffeurs d’Allocab.com sont pour la plus part
forfaitaires. Ils ne dépendent donc pas du temps engagé pour effectuer la course.</p>

<p>Ils sont rappelés dans le tableau ci-dessous. Les prix sont exprimés TTC. (TVA 5,5%)</p>


<h3>4- Conditions de paiement</h3>
<p>La prestation de Allocab.com est entendue en euros et TTC (TVA 5.5%).
Pour toute course, il sera remis au passager une facture.</p>

<p>Pour les clients occasionnels, le passager devra s’acquitter du paiement de la course
auprès du pilote à l’issue de la course. Le règlement pourra être effectué par Chèque,
Carte Bancaire ou espèce.</p>

<p>Pour les clients abonnés, ces derniers pourront s’acquitter de la somme soit directement
auprès du chauffeur de moto-taxis, soit par le biais de la plateforme Allocab.com. Dans
ce cas, le client s’engage à régler Allocab.com à réception de facture. Toute facture
non payée dans les 30 jours suivant sa réception, entraîne de plein droit l’application de
pénalités d’un montant égal à deux fois le taux d’intérêt légal en vigueur.</p>

<p>A défaut de paiement à l’échéance ou en cas de paiement incomplet des sommes
restant dues, Allocab.com pourra sans délai suspendre ses engagements contractuels.</p>

<h3>5- Annulation – Retard du passager</h3>
<p>En cas d’annulation de la course de la part du passager ou de la part du client, aucune
somme ne sera dû à Allocab.com, et ce, jusqu’à 2h avant la course. Passé ce temps,
allocab.com se réserve le droit de facturer la moitié de la somme dû, ou bien imposera
un malus au client applicable dès sa prochaine commande.</p>

<p>Par ailleurs, allocab.com ne peut s’engager à plus de vingt minutes d’attente sans
notification préalable de la part de client, soit sur allocab.com, soit directement
auprès du Chauffeur de Moto-Taxis. Après un délai d’attente de vingt minutes et sans
notification préalable, le conducteur sera en droit de considérer que la réservation
a été annulée. Dans ce cas là, allocab.com se réserve le droit de facturer la totalité de la somme dû, ou bien imposera un malus au client applicable dès sa prochaine
commande.</p>

<p>Pour tout retard de train et d’avion de plus de trente minutes sans notification, une
astreinte d’un 1 euro par minute sera facturée au-delà de la trentième minute.</p>

<p>En cas de notification de retard du client, le Chauffeur de Moto-Taxi attendra ce dernier
20 minutes sans surplus de facturation. A partir de ce délai, le prix de la course sera
majoré de 5€ par tranche de 10 minutes.</p>

<h3>6- Retard du Chauffeur</h3>

<p>En cas de retard du chauffeur mis à disposition par Allocab.com par rapport à l’heure de
prise en charge et sauf accord de décalage prévue, il sera déduit une remise de 10%
sur le prix de la course par tranche de 10 minutes de retard. Cette remise ne s’applique
qu’à partir de la 11ème minute de retard du chauffeur.</p>

<p>Il est expressément convenu que les retards sur les délais de transports dus à des
difficultés imprévues de circulation (embouteillage, accidents, déviations, conditions
météo, évènements divers relevant de la force majeure) n’ouvrent droit à aucune
indemnité pour le client ou le passager.</p>

<h3>7- Equipement mis à disposition du passager</h3>

<p>La course effectuée par Allocab.com sera effectuée en Honda Goldwing 1800 cc ou
équivalent.
Le chauffeur devra :
- demander au passager avant le départ de la course si celui-ci à une
préférence sur son itinéraire,
- proposer au passager d’écouter la fréquence radio de son choix ou de
brancher son ipod si le passager le préfère,
- mettre à disposition du passage une combinaison anti-pluie,
- disposer d’un casque pilote et passager amovible (intégral homologué),
- respecter la confidentialité la plus totale par rapport aux discussions
éventuelles avec le passager lors des coures</p>

